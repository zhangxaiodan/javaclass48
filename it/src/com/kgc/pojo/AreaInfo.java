package com.kgc.pojo;

public class AreaInfo {
	private int arid;//地区编号
	private String arprovince;//省名称
	private String arcity;//市名称
	private String ardistract;//区县名称
	private String arcode;//邮政编码
	private String arlng;//经度
	private String arlat;//纬度
	
	public int getArid() {
		return arid;
	}
	public void setArid(int arid) {
		this.arid = arid;
	}
	public String getArprovince() {
		return arprovince;
	}
	public void setArprovince(String arprovince) {
		this.arprovince = arprovince;
	}
	public String getArcity() {
		return arcity;
	}
	public void setArcity(String arcity) {
		this.arcity = arcity;
	}
	public String getArdistract() {
		return ardistract;
	}
	public void setArdistract(String ardistract) {
		this.ardistract = ardistract;
	}
	public String getArcode() {
		return arcode;
	}
	public void setArcode(String arcode) {
		this.arcode = arcode;
	}
	public String getArlng() {
		return arlng;
	}
	public void setArlng(String arlng) {
		this.arlng = arlng;
	}
	public String getArlat() {
		return arlat;
	}
	public void setArlat(String arlat) {
		this.arlat = arlat;
	}
}
