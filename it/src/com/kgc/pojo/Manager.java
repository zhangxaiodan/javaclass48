package com.kgc.pojo;
/**
 * 管理员
 * @author Administrator
 *
 */
public class Manager {
	private int maId;//管理员编号
	private String maName;//管理员用户名
	private String maPwd;//密码
	private String maEmail;//邮箱
	
	
	
	public int getMaId() {
		return maId;
	}
	public void setMaId(int maId) {
		this.maId = maId;
	}
	public String getMaName() {
		return maName;
	}
	public void setMaName(String maName) {
		this.maName = maName;
	}
	public String getMaPwd() {
		return maPwd;
	}
	public void setMaPwd(String maPwd) {
		this.maPwd = maPwd;
	}
	public String getMaEmail() {
		return maEmail;
	}
	public void setMaEmail(String maEmail) {
		this.maEmail = maEmail;
	}
	
	

}
